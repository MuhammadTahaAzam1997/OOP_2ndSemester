class Q14
{
public static void main(String args[]){
System.out.println(Math.abs(-20.1)); //It takes the absolute value and it will be in positive value
System.out.println(Math.min(15,7));  //It will take the minimum number between 15,7
System.out.println(Math.max(15,7));  // It will take the maximum number between 15,7
System.out.println(Math.ceil(1.6));  // It will make the number around the next digits
System.out.println(Math.floor(1.6));  // It will make the number around the previous digits
System.out.println(Math.pow(5,5));   // Power of number
System.out.println(Math.random());  // Random from 0.0 to < 0.1
System.out.println(Math.sqrt(9));  // it will take the square
}
}